  }

  static String bineoAccountNumber(
    String accountNumber, {
    required bool isHidden,
  }) {
    List<String> accountParts = [];

    for (int i = 0; i < accountNumber.length; i++) {
      if (accountParts.isEmpty || accountParts.last.length == 4) {
        accountParts.add(accountNumber[i]);
      } else {
        accountParts.last = '${accountParts.last}${accountNumber[i]}';
      }
    }

    if (isHidden) {
      accountParts.first = '****';
    }

    return 'Cuenta bineo ${accountParts.join(' ')}';
  }
